export const LowercaseAlphabetCharset = "abcdefghijklmnopqrstuvwxyz";
export const UppercaseAlphabetCharset = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
export const AlphebeticCharset = LowercaseAlphabetCharset+UppercaseAlphabetCharset;
export const NumericCharset = "0123456789";
export const AlphanumericCharset = AlphebeticCharset+NumericCharset;
export const JavascriptIdentifierCharset = AlphebeticCharset+NumericCharset+"$_";
export const WhitespaceCharset = " \n\r\t\f";